class  CharNarrowconversion
{
	public static void main(String[] args) 
	{
	  char ch = 'S';
	  byte b = (byte) ch;
	  System.out.println(b);
	  short s = (short) ch;
	  System.out.println(s);
      float f = (float) ch;
	  System.out.println(f);
      double d = (double) ch;
	  System.out.println(d);
	}
}
